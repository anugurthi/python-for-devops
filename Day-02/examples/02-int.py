# Integer variables (whole numbers)
num1 = 10
num2 = 5

# Integer Division (//) gives the whole number result
result1 = num1 // num2
print("Integer Division:", result1)  # Output: 2

# Modulus (%) gives the remainder after division
result2 = num1 % num2
print("Modulus (Remainder):", result2)  # Output: 0

# Absolute Value (abs) gives the positive version of a number
result3 = abs(-7)
print("Absolute Value:", result3)  # Output: 7
